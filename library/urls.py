from django.urls import path, include
from rest_framework.routers import DefaultRouter
from .views import BookViewSet, CategoryViewSet, AvailableBooksView

# Initialize the router
router = DefaultRouter()

# Register viewsets with the router
router.register(r'categories', CategoryViewSet, basename='category')
router.register(r'books', BookViewSet, basename='book')


# Define the urlpatterns
urlpatterns = [
    path('', include(router.urls)),  # Automatically include the routes generated by the router
    
    # Custom URL for available books (those with quantity > 0)
    path('available_books/', AvailableBooksView.as_view(), name='available-books'),
]
